package basics;

public class DataTypes {
    public static void main(String[] args) {
        // Integer types
        byte byteVal = 120;           // 1 byte
        short shortVal = 32000;       // 2 bytes
        int intVal = 54;              // 4 bytes
        long longVal = 9876543210L;   // 8 bytes (notice the L at the end)

        // Floating-point type
        float floatVal = 3.14f;       // 4 bytes (notice the f at the end)
        double doubleVal = 99.99;     // 8 bytes

        // Character type
        char charVal = 'A';           // 2 bytes

        // Boolean type
        boolean boolVal = true;       // 1 bit (true or false)

            }
}
